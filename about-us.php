    <td><h2>Halodoc Has Successfully Completed Series D Funding Round, Serving the Nation with more Accessible Healthcare</h2>
    28 Juli 2023</td></tr>
<tr align="center">
    <td><img src="image/halodoc funding.jpg" align="alt="funding" width="450" height="300"></td></tr>
</table>
<table>
<tr><td>Jakarta, 28 July 2023 – Halodoc, a leading digital healthcare ecosystem platform in Indonesia, has successfully completed a series D funding round. Our mission in simplifying healthcare access has resonated with respected partners that have the same value, to democratize healthcare access for the nation. This funding round is led by PT Astra International Tbk (Astra) through its subsidiary, PT Astra Digital Internasional, while we are also trusted by Openspace and Novo Holdings. The new investment and collaboration will be instrumental in accelerating the company’s mission to help more people to get accessible, reliable, and quality healthcare.
    Reflecting on the past years, we have witnessed the impact of telemedicine in addressing patients’ pain points, particularly during the COVID-19 pandemic, where more than 40% of patient utilized telemedicine, with 96% of them redeemed e-prescription digitally according to the Ministry of Health’s data (ISOMAN Program). This speaks volumes about telehealth solutions being relevant and able to cater the needs of users. 
    <br/><br/>
    CEO & Co-Founder of Halodoc Jonathan Sudharta said, “We would like to thank our partners for their enormous support and trust since the early inception of Halodoc. Impact that has been created by Halodoc is also a testament of partners’ commitment to support the acceleration of digital adoption in healthcare sectors. We also genuinely appreciate Astra, Openspace, Novo Holdings, as well as our early investors like Acrew Capital, Argor (previously Go-Ventures), Allianz X, Bangkok Bank, Bill & Melinda Gates Foundation, HEAL Partners, Intudo Ventures, Korea Investment Partners, Prudential, TMI (Telkomsel venture arm), Singtel Innov8, UOB Venture Management, Uni-President, Global Investama Andalan, and InvestIdea for the trust given to us. The support definitely enables us to develop innovation and serve more people.”
    President Director of Astra Djony Bunarto Tjondro, said, “High demand for access to quality healthcare services has boosted the increasing adoption of digital technology in healthcare. Astra believes that the health sector in Indonesia has good growth prospects in the long term. This is also supported by the Government’s efforts to advance the healthcare sector in the country. We hope that Astra’s investment in Halodoc can accelerate the transformation of health services in Indonesia which are increasingly innovative, accessible and providing quality service.” 
    <br/><br/>
    Partner at Openspace Shane Chesson also expressed the support saying that, ”Openspace is very proud to participate in this funding round and continue the partnership with Halodoc which kicked off in 2017. We are among a group of experienced local and international investors supporting its journey in meeting Indonesia’s intense demand for quality and efficient healthcare. Digital health options are becoming the preferred choice for patients and Halodoc leads the way, having built a relationship of trust and a breadth of services that drives special insights.”
    Moreover, Partner at Novo Holdings Navjeewan Khosla, stated, “Halodoc is improving affordability and access to healthcare in Indonesia with its robust digital technology and wide network of providers and partnerships. The company’s successful track record with national vaccination campaigns and Covid-19 vaccination response, and their ability to serve more than 20 million patients with convenient and high-quality healthcare is a strong foundation for sustainable growth.”
    <br/><br/>
    As we embrace the endemic era, we remain committed to promoting wellness through comprehensive healthcare services (ranging from preventive to curative) offered within our all-in-one application. In addition to our efforts to enhance digital health literacy among the Indonesian through health communication, education, and information, Halodoc also continues to develop and expand with a variety of services, making it easier for people to access healthcare services, such as: 1) Home Lab – a preventive healthcare service that enables users to safely and conveniently carry out health tests from the comfort of their homes. 2) My Insurance – integrated with leading insurance partners, allowing users to access the benefits of cashless outpatient services in a more easy and seamless manner.
    Halodoc is also working closely with the government, to accelerate technological advancement in the healthcare industry, including through the initiation of telehealth regulation as outlined in the Ministry of Health (MoH) of the Republic of Indonesia. The MoH has implemented the Regulatory Sandbox to facilitate Digital Health Innovation (IDK) organizers such as Halodoc. Through this Regulatory Sandbox, MoH provides a ‘safe space’ for Halodoc and other IDK organizers to partner with the government in developing sustainable and high-quality innovations, whilst ensuring user safety as a top priority – Halodoc is now ISO 27001 and ISO 27701 certified. 
    <br/><br/>
    Beyond the numbers of the investment lies our true passion and responsibility to solve patient’s pain points. Halodoc extends our gratitude to everyone who has been part of our journey – HaloSquad, our visionary partners, stakeholders, and our loyal supporters. Together, we are committed to striving to bring a positive impact to the health industry towards a future where every individual can have accessible, reliable, and quality healthcare
    <br/><br/>
